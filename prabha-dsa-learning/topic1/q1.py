#Declare and initialize the array
numbers = [5,10,15,20,25]

#Get total number of elements in the array
total_elements = len(numbers)

#print the total number of elements
print("Total number of elements in the array:", total_elements)

#len is a built-in function in Python that returns the number of items in an object.
#In this case, it returns the number of elements in the 'numbers' array.
#The output will be: Total number of elements in the array: 5

